require('dotenv').config();
const { zoyaAgent } = require('../ai/graph/index');

async function run() {
    console.log('--- TEST V2 GRAPH SPRINT 4 (E2E WITH LLM) ---');
    
    // We pass a dummy state but the LLM will actively parse it
    const initialState = {
        messages: [{ role: 'user', content: 'Mas, berapa harga repaint bodi halus buat motor Beat?' }],
        metadata: {
            phoneReal: '6288888888888',
            isAdmin: false,
            thread_id: 'test-thread-sprint4'
        },
        consultation: {
            stage: 'DISCOVERING',
            missingFacts: []
        },
        conversation: {
            buyerStage: 'Exploring',
            status: 'active'
        },
        memory: {
            identity: {},
            salesMemory: {}
        }
    };

    const config = {
        configurable: {
            thread_id: 'test-thread-sprint4'
        }
    };

    try {
        console.log('Invoking Agent...');
        const result = await zoyaAgent.invoke(initialState, config);
        
        console.log('\n--- EXECUTION RESULT ---');
        console.log('Planner Decision:');
        console.log(result.planner);

        console.log('\nTool Execution:');
        console.log(result.tool?.lastResult);

        console.log('\nComposer Response (Generated by Gemini):');
        const msgs = result.conversation?.lastMessages || [];
        console.log(msgs[msgs.length - 1]);

        console.log('\n--- MEMORY TRACKER (Generated by Gemini) ---');
        console.log('Identity:', result.memory?.identity);
        console.log('Sales Memory:', result.memory?.salesMemory);
        
        console.log('\n--- ANALYTICS ---');
        console.log('Buyer Stage:', result.analytics?.buyerStage);

        console.log('\nExecution successful!');
    } catch (err) {
        console.error('Execution Failed:', err);
    }
}

run().then(() => process.exit(0)).catch(err => {
    console.error(err);
    process.exit(1);
});
